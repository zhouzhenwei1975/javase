package net.zzw.javase.foundational.control.forstatement;

/**
 * for循环输出5个HelloWorld
 *
 * @author zzw dgzzw1@163.com
 * @version 2021/12/12 下午7:11
 * @since JDK11
 */
public class ForHelloWorld {

	public static void main(String[] args) {

		for (int i = 0; i < 5; i++) {
			System.out.println("Hello World");
		}
	}
}

package net.zzw.javase.foundational.control.forstatement;

/**
 * 打印输出100-1的整数
 *
 * @author zzw dgzzw1@163.com
 * @version 2021/12/12 下午9:26
 * @since JDK11
 */
public class For100To1 {
	public static void main(String[] args) {

		for (int i = 100; i >= 1; i--) {
			System.out.print(i + " ");
		}
	}
}

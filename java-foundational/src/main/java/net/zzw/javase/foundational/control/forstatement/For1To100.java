package net.zzw.javase.foundational.control.forstatement;

/**
 * 打印输出1-100的整数
 *
 * @author zzw dgzzw1@163.com
 * @version 2021/12/12 下午9:24
 * @since JDK11
 */
public class For1To100 {
	public static void main(String[] args) {

		// 需求：往控制打印输出1-100的整数

		for (int i = 1; i <= 100; i++) {
			System.out.print(i + " ");
		}
	}
}
